                    $percentage = min(round($percentage, 2), 100);

                    if ($score > $bestScoreForWord) {
                        $bestScoreForWord = $score;
                        $bestMaxPossibleForWord = $maxPossible;
                        $bestPercentageForWord = $percentage;
                    }
                }

                $totalScore += $bestScoreForWord;
                $totalMaxPossibleGlobal += $bestMaxPossibleForWord;
                $totalPercentage += $bestPercentageForWord;
                $wordCount++;
            }

            $globalPercentage = $wordCount > 0 ? round($totalPercentage / $wordCount, 2) : 0;

            if ($totalScore > 0) {
                $results[] = [
                    'name' => $item,
