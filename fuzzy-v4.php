    private function getAllSubstrings($word)
    {
        $length = strlen($word);
        $substrings = [];
        for ($i = 0; $i < $length; $i++) {
            for ($j = $i + 1; $j <= $length; $j++) {
                $substrings[] = substr($word, $i, $j - $i);
            }
        }

        return array_unique($substrings);
    }

    private function getWeight($tokenLength)
    {
        if ($tokenLength <= 1) {
            return $tokenLength;
        }

        return $tokenLength + (($tokenLength - 1) * 0.5);
    }

    private function getMaxPossibleScore($word)
    {
        $wordNormalized = strtolower($this->cleanString($word));
        $tokens = $this->getAllSubstrings($wordNormalized);
        $maxScore = 0;

        foreach ($tokens as $token) {
            if (preg_match('/'.preg_quote($token, '/').'/', $wordNormalized)) {
                $maxScore += $this->getWeight(strlen($token));
            }
        }

        return round($maxScore, 1);
    }

    private function scoreMot($mot1, $mot2)
    {
        $score = 0;
        $mot1Cleaned = strtolower($this->cleanString($mot1));
        $mot2Cleaned = strtolower($this->cleanString($mot2));

        $tokens = $this->getAllSubstrings($mot1Cleaned);

        foreach ($tokens as $token) {
            if (preg_match('/'.preg_quote($token, '/').'/', $mot2Cleaned)) {
                $score += $this->getWeight(strlen($token));
            }
        }

        return $score;
    }

